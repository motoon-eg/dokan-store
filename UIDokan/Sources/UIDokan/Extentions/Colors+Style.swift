    static var redVelvet: UIColor {
        return UIColor(named: "Red Velvet")!
    }

    static var halfGrey: UIColor {
        return UIColor(named: "Half Grey")!
    }
    
    static var offGrey: UIColor {
        return UIColor(named: "Off Grey")!
    }

}
